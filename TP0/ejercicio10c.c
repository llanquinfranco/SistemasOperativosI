#include <stdio.h>

int main() {
    printf("%d\n", 20 / 3);
    printf("%f\n", (float)20 / 3);
    printf("%f\n", 20 / 3.);
    printf("%d\n", 10 % 3);
    printf("%d\n", (int)3.1416);
    printf("%lf\n", (double)20 / 3);
    printf("%d\n", (int)3.1416);
    printf("%d\n", (int)3.1416);
    return 0;
}